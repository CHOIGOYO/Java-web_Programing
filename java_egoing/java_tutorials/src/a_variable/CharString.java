package a_variable;

public class CharString {

    public static void main(String[] args) {
        // 문자와 문자열표기방법 //
        
        //한 글자는 문자(char) ''를 사용해준다
        System.out.println('꿀');
        
        //문자가 결합되어있기 때문에 문자열(string) ""를 사용해준다
        //문자열은 작은따옴표 ''를 사용하면 오류가 발생한다
        System.out.println("꿀물과 오미자");
        
        //""를 사용하였기 때문에 문자열로 인식
        System.out.println("꿀");
        
        //이스케이프 \\안에 "apple" 포함 시키기 (\"~\")
        System.out.println("I said \"apple\"");
        
        
        //줄바꿈 하여 출력하기 (\n)
        System.out.println("I said\n\"apple\"");
        
        //문자의 연산
        System.out.println("my name is "+"kkk");
        
        
        
        
        
    }

}