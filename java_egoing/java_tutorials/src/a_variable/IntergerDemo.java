package a_variable;

public class IntergerDemo {

	public static void main(String[] args) {
		/*
		 * 변부를 선언할 때 
		 * (데이터 형식) (변수의 이름); 
		 * (변수의 이름) = (대입할 값)
		 */
		
		int a; //변수 a의 데이터 형식을 선언
		a = 1; //변수 a의 값 지정
		
		System.out.println(a); // 1
		
		
		a = 3; //변수 a의 값 변경
		System.out.println(a); //3 
	}

}
