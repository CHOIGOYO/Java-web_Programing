package a_variable;

public class DoubleDemo {

	public static void main(String[] args) {
		// 정수와 실수
		
		
		double a; // 변수 a의 데이터형식은 (실수형)double임을 선언
		a = 1.1; //변수 a의 값은 1.1 지정
		
		/*
		 아래와 같이 변수를 선언할 때, 데이터 형식이 맞지 않으면 오류가 발생한다
		 int a;  (int는 정수형 데이터타입)   
		 a = 1.1;
		*/
		
		System.out.println(a);
		System.out.println(a + 1.1);
		
	}

}
