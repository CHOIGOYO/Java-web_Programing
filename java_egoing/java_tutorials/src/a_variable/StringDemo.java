package a_variable;

public class StringDemo {

	public static void main(String[] args) {
		// 문자열과 변수
		
		String first; // 변수 frist에 string(문자열)데이터 타입만 들어가도록 강제
		first = "String은 문자열 데이터."; //변수에 값 할당
		
		System.out.println(first);
		
		//한줄로 작성하기
		String second ="변수의 데이터타입과 값의 할당을 한줄로 작성하기.";
		
		System.out.println(second);
		
		// String변수 연산
		String a,b;
		a = "coding is ";
		b = "everybody";
		
		System.out.println(a+b);
		
		
				
		
	}

}
