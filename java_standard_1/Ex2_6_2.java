package java_standard_1;

public class Ex2_6_2 {

	public static void main(String[] args) {
		// 상수와 리터럴 
		
		final int MAX_VALUE; // (final을 변수타입 앞에 작성해주면 상수로 선언이됨)
		//정수형 상수 MAX_VALUE선언 상수의 이름은 대문자로 작성하는 것이 관례
		
		MAX_VALUE = 100; // 상수의 값은 한번 지정하면 변경하지 못한다 
		
		System.out.println(MAX_VALUE);
	}

}
